     function getRandomColor() {
      const letters = '0123456789ABCDEF';
      let color = '#';
      for (let i = 0; i < 6; i++) {
        color += letters[Math.floor(Math.random() * 16)];
      }
      return color;
    }

    function generateBarChartColors(dataLength) {
      const colors = [];
      for (let i = 0; i < dataLength; i++) {
        colors.push(getRandomColor());
      }
      return colors;
    }
